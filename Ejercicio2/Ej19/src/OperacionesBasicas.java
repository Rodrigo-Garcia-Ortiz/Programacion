public class OperacionesBasicas {

    public static int sumar(int a, int b) { //Suma
        return a + b;
    }

    public static int restar(int a, int b) { //Resta
        return a - b;
    }

    public static int multiplicar(int a, int b) { //Multiplica
        return a * b;
    }

    public static int dividir(int a, int b) { //Divide
        return a / b;
    }
}